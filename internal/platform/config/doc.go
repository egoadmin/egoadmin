// Package config 是多源配置编排器。
//
// 它按优先级合并多个配置源（内置默认 < 文件 < 数据库覆盖）并应用环境变量覆盖，
// 产出两份结果：
//   - 渲染后的临时配置文件，供 ego 框架通过 --config 读取（见 Config.RenderedPath）；
//   - 强类型的 *Config，供业务代码使用（见 Config 各 getter）。
//
// 任一可热重载的源（如文件、数据库）变化时，自动重新合并、覆写临时文件并重新绑定 *Config。
//
// 本包不依赖 ego 的全局 econf：自己读取源文件、自己监听变化。ego 框架只通过临时文件
// 被动读取配置，两者仅以该临时文件为唯一接触点，从而避免与 ego 配置加载时序、
// econf 锁产生耦合。
